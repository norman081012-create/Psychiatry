import time

def process_transcript(transcript, school_type, patient_info=None):
    """模擬後端 LLM 根據不同學派與個案資料進行分析與匯總"""
    if not transcript.strip():
        return None, None
        
    time.sleep(1.5) # 模擬 API 呼叫延遲
    
    # --- 未來串接真實 LLM 的 Prompt 範例 ---
    # prompt = f"""
    # 你是一位資深的臨床心理師。請使用 {school_type} 視角進行分析。
    # 【個案基本資料】：{patient_info}
    # 【醫病對談逐字稿】：{transcript}
    # 
    # 請輸出兩部分：
    # 1. 該學派的重點分析報告（條列式）。
    # 2. 心理師匯總：結合分析報告與個案基本資料，撰寫約 500 字、分為兩段的臨床概念化與處遇計畫總結。
    # """
    
    # 1. 取得個案基本資料 (處理空值)
    if not patient_info:
        patient_info = {}
    p_name = patient_info.get("name", "個案")
    p_name = p_name if p_name else "個案"
    p_age = patient_info.get("age", "未知")
    p_job = patient_info.get("job", "未知職業")
    p_job = p_job if p_job else "未知職業"
    p_history = patient_info.get("history", "無特殊病史")
    p_history = p_history if p_history else "無特殊病史"
    
    # 2. 模擬各學派輸出的「分析報告」假資料字典
    mock_reports = {
        "CBT": "### 認知行為治療 (CBT) 分析報告\n* **認知扭曲：** 讀心術/災難化，預設主管會挑毛病。\n* **惡性循環：** 擔心出錯(想法) -> 焦慮恐慌(情緒) -> 心悸失眠(生理) -> 放棄休閒/對伴侶發脾氣(行為)。\n* **介入建議：** 認知重構、行為活化、放鬆訓練。",
        "Psychodynamic": "### 精神動力學派 (Psychodynamic) 分析報告\n* **防衛機轉：** 轉移 (Displacement)，將對權威的挫折發洩在伴侶身上。\n* **潛意識衝突：** 超我過度嚴苛，對新主管的恐懼投射了過去未解決的權威議題。\n* **介入建議：** 探索情感轉移，將無意識衝突意識化。",
        "Humanistic": "### 人本與存在主義 (Humanistic & Existential) 分析報告\n* **條件性正向關懷：** 價值過度綑綁於主管評價，真實自我與理想自我落差巨大。\n* **意義感喪失：** 日常活動失去活力，感受到強烈的失控感。\n* **介入建議：** 提供無條件正向關懷，協助找回內在評價中心。",
        "Third-Wave": "### 第三波療法 (ACT/MBCT/DBT) 分析報告\n* **認知融合：** 與「我會被挑毛病」的想法緊密黏著，視為絕對現實。\n* **經驗迴避：** 試圖逃避焦慮，導致交感神經過度活化。\n* **介入建議：** 認知解離、正念覺察身體感受、價值觀釐清與承諾行動。"
    }
    report = mock_reports.get(school_type, "無分析結果")

    # 3. 模擬 LLM 生成的「心理師匯總 (約500字，分兩段)」
    summary = f"""
### 👨‍⚕️ 心理師臨床匯總與概念化 ({school_type} 視角)

{p_name}（{p_age}歲，{p_job}）近期呈現高度的焦慮與身心耗竭狀態。結合其背景資料與本次會談逐字稿，個案的心理困擾與其近期生活或職場人際變動有密切關聯。在面臨外在壓力時，個案表現出強烈的自我要求與對失敗的恐懼，這些反應不僅影響了其社會功能，更進一步蔓延至親密關係中，導致情緒失控與伴侶衝突。從 {school_type} 的視角來看，個案當前的心理痛苦源自於其深層的防衛機制或僵化的認知模式，且伴隨著過去「{p_history}」的背景影響，使其在面對評價或未知時，容易陷入自我價值感低落與過度警覺的惡性循環。個案目前的自我調節機制已經超載，出現了睡眠剝奪、心悸等生理警訊，顯示其急需外在的專業介入與支持來緩解當前的危機狀態。

在後續的諮商計畫中，治療目標將首重於協助 {p_name} 建立穩定的身心容納之窗。首先，透過 {school_type} 的核心技術，引導個案覺察自身在壓力情境下的自動化反應與情緒盲點，並探索這些反應背後的潛在驅力與非理性信念。治療師需要在會談中提供一個安全、接納的涵容空間，修復其受損的自我價值感。隨著治療的深入，將逐步挑戰其僵化的行為模式，協助個案發展出更具彈性的因應策略，例如：重新建立人際界線、練習情緒表達與自我安撫技巧。預期經過一段時間的會談，個案能將諮商中獲得的覺察帶入日常生活中，不僅能改善周遭的人際與伴侶關係，更重要的是能重新找回對自我生命的掌控感與真實的連結。
    """
    
    return report, summary
